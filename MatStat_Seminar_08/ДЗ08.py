# –¢–µ–æ—Ä–∏—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–µ–π –∏ –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∞—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ (—Å–µ–º–∏–Ω–∞—Ä—ã)
# –£—Ä–æ–∫ 8. –ö–æ—Ä—Ä–µ–ª—è—Ü–∏–æ–Ω–Ω—ã–π –∞–Ω–∞–ª–∏–∑
from math import sqrt
import numpy as np
import pylab
import scipy.stats as stats
import matplotlib.pyplot as plt

# –ó–∞–¥–∞—á–∞ 1. –î–∞–Ω—ã –∑–Ω–∞—á–µ–Ω–∏—è –≤–µ–ª–∏—á–∏–Ω—ã –∑–∞—Ä–∞–±–æ—Ç–Ω–æ–π –ø–ª–∞—Ç—ã –∑–∞–µ–º—â–∏–∫–æ–≤ –±–∞–Ω–∫–∞ (zp) –∏ –∑–Ω–∞—á–µ–Ω–∏—è –∏—Ö –ø–æ–≤–µ–¥–µ–Ω—á–µ—Å–∫–æ–≥–æ –∫—Ä–µ–¥–∏—Ç–Ω–æ–≥–æ —Å–∫–æ—Ä–∏–Ω–≥–∞ (ks).
print(f'–ó–∞–¥–∞—á–∞ 1.')
# –ù–∞–π–¥–∏—Ç–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é —ç—Ç–∏—Ö –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω —Å –ø–æ–º–æ—â—å—é —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π, –∞ –∑–∞—Ç–µ–º —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ cov –∏–∑ numpy
# –ü–æ–ª—É—á–µ–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —Ä–∞–≤–Ω—ã.
# –ù–∞–π–¥–∏—Ç–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ —Å –ø–æ–º–æ—â—å—é –∫–æ–≤–∞—Ä–∏–∞—Ü–∏–∏ –∏ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω—ã—Ö –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–π –¥–≤—É—Ö –ø—Ä–∏–∑–Ω–∞–∫–æ–≤, –∞ –∑–∞—Ç–µ–º —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Ñ—É–Ω–∫—Ü–∏–π –∏–∑ –±–∏–±–ª–∏–æ—Ç–µ–∫ numpy –∏ pandas.

zp_list = [35, 45, 190, 200, 40, 70, 54, 150, 120, 110]
ks_list = [401, 574, 874, 919, 459, 739, 653, 902, 746, 832]

zp = np.array(zp_list)
ks = np.array(ks_list)

# –û—Ç—Ä–∏—Å—É–µ–º –≥—Ä–∞—Ñ–∏–∫
plt.scatter(zp, ks)
plt.show()

# 1) –°–º–µ—â—ë–Ω–Ω–∞—è –∏ –Ω–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏–∏ —ç—Ç–∏—Ö –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω —Å –ø–æ–º–æ—â—å—é —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π

sum01 = sum02 = sum03 = 0
count01 = count02 = count03 = 0

for i in zp_list:
    sum01 += i
    count01 += 1

mean_zp_manual01 = sum01 / count01

for i in ks_list:
    sum02 += i
    count02 += 1

mean_ks_manual01 = sum02 / count02

zp_ks_list01 = list(zip(zp_list, ks_list))

for i in range(len(zp_ks_list01)):
    sum03 += zp_ks_list01[i][0]*zp_ks_list01[i][1]
    count03 += 1

mean_zp_ks_manual01 = sum03/count03

# Sample Variance (N-1) vs. Population Variance (N): What‚Äôs the Difference?
# https://www.statology.org/sample-variance-vs-population-variance/
#
# KB442275: Covariance function uses Sample Covariance (N-1) formula in Analytical Engine instead of Population Covariance (N)
# https://community.microstrategy.com/s/article/KB442275-Covariance-function-uses-Sample-Covariance-formula-in-Analytical-Engine-instead-of-Population-Covariance?language=en_US

# –°–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω (Population Covariance (N))
# cov_xy=M(XY)‚àíM(X)‚àóM(Y) - —Å–º–µ—â–µ–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è (ddof=0)
pop_cov_manual01 = mean_zp_ks_manual01 - (mean_zp_manual01 * mean_ks_manual01)

print(f'–°–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω —Å –ø–æ–º–æ—â—å—é —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π —Ä–∞–≤–Ω–∞ {round(pop_cov_manual01, 5)}')
print(f'–°–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ cov –∏–∑ numpy —Ä–∞–≤–Ω–∞ {np.cov(zp, ks, ddof=0)}')

# –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω (Sample Covariance (N-1))
# –ù–µ–æ–±—Ö–æ–¥–∏–º–æ –¥–æ–º–Ω–æ–∂–∏—Ç—å –Ω–∞ n/n-1, –∫–∞–∫ –≤ —Å–ª—É—á–∞–µ —Å –¥–∏—Å–ø–µ—Ä—Å–∏–µ–π. n - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø–∞—Ä–Ω—ã—Ö —Å—Ä–∞–≤–Ω–µ–Ω–∏–π, –∏–ª–∏ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –ø–æ –æ–¥–Ω–æ–π –≤—ã–±–æ—Ä–∫–µ, –µ—Å–ª–∏ –æ–Ω–∏ –æ–¥–∏–Ω–∞–∫–æ–≤–æ–≥–æ –æ–±—ä–µ–º–∞. –í –Ω–∞—à–µ–º —Å–ª—É—á–∞–µ n = 10.
# cov_xy=(M(XY)‚àíM(X)‚àóM(Y))*(n/(n-1)) - –Ω–µ—Å–º–µ—â–µ–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è (ddof=1)
sample_cov_manual01 = (mean_zp_ks_manual01 - (mean_zp_manual01 * mean_ks_manual01))*(count03/(count03-1))

print(f'–ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω —Å –ø–æ–º–æ—â—å—é —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π —Ä–∞–≤–Ω–∞ {round(sample_cov_manual01, 5)}')
print(f'–ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –¥–≤—É—Ö –≤–µ–ª–∏—á–∏–Ω —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ cov –∏–∑ numpy —Ä–∞–≤–Ω–∞ {np.cov(zp, ks, ddof=1)}')

# 2)  –°–º–µ—â—ë–Ω–Ω–∞—è –∏ –Ω–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏–∏ –≤—ã–±–æ—Ä–æ–∫ —Ä–∞–≤–Ω–∞
sum04 = 0
count04 = 0
for i in zp_list:
    sum04 += (i - mean_zp_manual01)**2
    count04 += 1
# –°–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ zp —Ä–∞–≤–Ω–∞
pop_var_zp_manual01 = sum04 / count04
# –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ zp —Ä–∞–≤–Ω–∞
sample_var_zp_manual01 = sum04 / (count04 - 1)

sum05 = 0
count05 = 0
for i in ks_list:
    sum05 += (i - mean_ks_manual01)**2
    count05 += 1
# –°–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ ks —Ä–∞–≤–Ω–∞
pop_var_ks_manual01 = sum05 / count05
# –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ ks —Ä–∞–≤–Ω–∞
sample_var_ks_manual01 = sum05 / (count05 - 1)

# 3) C–º–µ—â—ë–Ω–Ω–æ–µ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ (–°–ö–û) –≤—ã–±–æ—Ä–æ–∫ —Ä–∞–≤–Ω–æ
# –°–º–µ—â—ë–Ω–Ω–∞—è –°–ö–û –≤—ã–±–æ—Ä–∫–∏ zp —Ä–∞–≤–Ω–∞
pop_std_zp_manual01 = pop_var_zp_manual01 ** 0.5
# –°–º–µ—â—ë–Ω–Ω–∞—è –°–ö–û –≤—ã–±–æ—Ä–∫–∏ ks —Ä–∞–≤–Ω–∞
pop_std_ks_manual01 = pop_var_ks_manual01 ** 0.5

# –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –°–ö–û –≤—ã–±–æ—Ä–∫–∏ zp —Ä–∞–≤–Ω–∞
sample_std_zp_manual01 = sample_var_zp_manual01 ** 0.5
# –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –°–ö–û –≤—ã–±–æ—Ä–∫–∏ ks —Ä–∞–≤–Ω–∞
sample_std_ks_manual01 = sample_var_ks_manual01 ** 0.5

# –ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º –Ω–µ—Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û) –¥–æ–ª–∂–µ–Ω —Å–æ–≤–ø–∞–¥–∞—Ç—å —Å –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–æ–º –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º –Ω–µ—Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û)

# –ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º —Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û)
corrcoef_zp_ks_manual01 = pop_cov_manual01 / (pop_std_zp_manual01 * pop_std_ks_manual01)

# –ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º –Ω–µ—Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û)
corrcoef_zp_ks_manual02 = sample_cov_manual01 / (sample_std_zp_manual01 * sample_std_ks_manual01)

print(f'–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º —Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û) —Å –ø–æ–º–æ—â—å—é —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π —Ä–∞–≤–µ–Ω {round(corrcoef_zp_ks_manual01, 5)}')
print(f'–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º –Ω–µ—Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û) —Å –ø–æ–º–æ—â—å—é —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π —Ä–∞–≤–µ–Ω {round(corrcoef_zp_ks_manual02, 5)}')
print(f'–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ –ü–∏—Ä—Å–æ–Ω–∞ (–±–µ—Ä—ë–º —Å–º–µ—â—ë–Ω–Ω—ã–µ –∏–ª–∏ –Ω–µ—Å–º–µ—â—ë–Ω–Ω—ã–µ –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—é –∏ –°–ö–û) —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ corrcoef –∏–∑ numpy —Ä–∞–≤–µ–Ω {np.corrcoef(zp, ks)}')


# –ó–∞–¥–∞—á–∞ 2. –ò–∑–º–µ—Ä–µ–Ω—ã –∑–Ω–∞—á–µ–Ω–∏—è IQ –≤—ã–±–æ—Ä–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, –æ–±—É—á–∞—é—â–∏—Ö—Å—è –≤ –º–µ—Å—Ç–Ω—ã—Ö —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–∏—Ö –≤—É–∑–∞—Ö.
print(f'\n–ó–∞–¥–∞—á–∞ 2.')
# –í—ã–±–æ—Ä–∫–∞
iq_list = [131, 125, 115, 122, 131, 115, 107, 99, 125, 111]
# –ò–∑–≤–µ—Å—Ç–Ω–æ, —á—Ç–æ –≤ –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ IQ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω –Ω–æ—Ä–º–∞–ª—å–Ω–æ.
# –ù–∞–π–¥–∏—Ç–µ –¥–æ–≤–µ—Ä–∏—Ç–µ–ª—å–Ω—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –¥–ª—è –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –æ–∂–∏–¥–∞–Ω–∏—è —Å –Ω–∞–¥–µ–∂–Ω–æ—Å—Ç—å—é 0.95.
# –†–µ—à–µ–Ω–∏–µ
# 1) –ù–∞–º –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ ùûº (–°–ö–û) –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ => –±–µ—Ä—ë–º t - –∫—Ä–∏—Ç–µ—Ä–∏–π (–∫—Ä–∏—Ç–µ—Ä–∏–π –°—Ç—å—é–¥–µ–Ω—Ç–∞)
print(f'–ù–∞–º –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ ùûº (–°–ö–û) –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ => –±–µ—Ä—ë–º t - –∫—Ä–∏—Ç–µ—Ä–∏–π (–∫—Ä–∏—Ç–µ—Ä–∏–π –°—Ç—å—é–¥–µ–Ω—Ç–∞)')
# 2) –í—ã–±–æ—Ä–∫–∞
print(f'–í—ã–±–æ—Ä–∫–∞ {iq_list}')
# 3) –†–∞–∑–º–µ—Ä –≤—ã–±–æ—Ä–∫–∏
print(f'–†–∞–∑–º–µ—Ä –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–µ–Ω {len(iq_list)}')
# 4) –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–µ–ø–µ–Ω–µ–π —Å–≤–æ–±–æ–¥—ã
print(f'–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–µ–ø–µ–Ω–µ–π —Å–≤–æ–±–æ–¥—ã —Ä–∞–≤–Ω–æ {len(iq_list) - 1}')
# 5) –°—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ
sum06 = 0
count06 = 0
for i in iq_list:
    sum06 += i
    count06 += 1
sample_mean_manual = sum06/count06
print(f'–ú–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–∂–∏–¥–∞–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ {sample_mean_manual}')
# 6)  –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–∞
sum07 = 0
count07 = 0
for i in iq_list:
    sum07 += (i - sample_mean_manual)**2
    count07 += 1
sample_var_manual = sum07/(count07 - 1)
print(f'–ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–∞ {sample_var_manual}')
# 7) –ù–µ—Å–º–µ—â—ë–Ω–Ω–æ–µ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ
sample_std_manual = sample_var_manual**0.5
print(f'–ù–µ—Å–º–µ—â—ë–Ω–Ω–æ–µ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ {sample_std_manual}')
