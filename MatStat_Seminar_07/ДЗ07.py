# –ó–∞–¥–∞—á–∞ 3. –°—Ä–∞–≤–Ω–∏—Ç–µ 1 –∏ 2 –µ –∏–∑–º–µ—Ä–µ–Ω–∏—è –∏–∑ –∑–∞–¥–∞—á–∏ 2, –ø—Ä–µ–¥–ø–æ–ª–∞–≥–∞—è, —á—Ç–æ 3–≥–æ –∏–∑–º–µ—Ä–µ–Ω–∏—è —á–µ—Ä–µ–∑ 30 –º–∏–Ω—É—Ç –Ω–µ –±—ã–ª–æ.
