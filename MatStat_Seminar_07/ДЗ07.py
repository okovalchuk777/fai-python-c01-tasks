# –ó–∞–¥–∞—á–∞ 4. –î–∞–Ω—ã 3 –≥—Ä—É–ø–ø—ã  —É—á–µ–Ω–∏–∫–æ–≤ –ø–ª–∞–≤–∞–Ω–∏—è.
# –í 1, 2, 3 –≥—Ä—É–ø–ø–∞—Ö –≤—Ä–µ–º—è –Ω–∞ –¥–∏—Å—Ç–∞–Ω—Ü–∏—é 50 –º —Å–æ—Å—Ç–∞–≤–ª—è—é—Ç:
# –ù–∞–º –∏–∑–≤–µ—Å—Ç–Ω–æ, —á—Ç–æ 1) –≤—ã–±–æ—Ä–æ–∫ –±–æ–ª—å—à–µ –¥–≤—É—Ö, 2) –≤—ã–±–æ—Ä–∫–∏ –Ω–µ–∑–∞–≤–∏—Å–∏–º—ã–µ (—Ä–ø–∑–Ω—ã–µ –≥—Ä—É–ø–ø—ã). ==> –ö—Ä–∏—Ç–µ—Ä–∏–π –ö—Ä—É—Å–∫–∞–ª–∞-–£–æ–ª–ª–∏—Å–∞

group1 = np.array([56, 60, 62, 55, 71, 67, 59, 58, 64, 67])
