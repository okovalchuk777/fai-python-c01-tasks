# –ó–∞–¥–∞—á–∞ 5. –ó–∞—è–≤–ª—è–µ—Ç—Å—è, —á—Ç–æ –ø–∞—Ä—Ç–∏—è –∏–∑–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è —Å–æ —Å—Ä–µ–¥–Ω–∏–º –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏–º 2,5 —Å–º. –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –¥–∞–Ω–Ω—É—é –≥–∏–ø–æ—Ç–µ–∑—É, –µ—Å–ª–∏ –∏–∑–≤–µ—Å—Ç–Ω–æ, —á—Ç–æ —Ä–∞–∑–º–µ—Ä—ã –∏–∑–¥–µ–ª–∏–π –ø–æ–¥—á–∏–Ω–µ–Ω—ã –Ω–æ—Ä–º–∞–ª—å–Ω–æ–º—É –∑–∞–∫–æ–Ω—É —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è. –û–±—ä–µ–º –≤—ã–±–æ—Ä–∫–∏ 10, —É—Ä–æ–≤–µ–Ω—å —Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–æ–π –∑–Ω–∞—á–∏–º–æ—Å—Ç–∏ 5%.

# –†–µ—à–µ–Ω–∏–µ
# 1) –ò—Å–ø–æ–ª—å–∑—É–µ–º t - –∫—Ä–∏—Ç–µ—Ä–∏–π (–∫—Ä–∏—Ç–µ—Ä–∏–π –°—Ç—å—é–¥–µ–Ω—Ç–∞) —Ç–∞–∫ –∫–∞–∫ ùûº (–°–ö–û) –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏  –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–∞
# 2) –°—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∞—è –∑–Ω–∞—á–∏–º–æ—Å—Ç—å —Ä–∞–≤–Ω–∞ 5 –ø—Ä–æ—Ü–µ–Ω—Ç (0.05), –ø—Ä–∏ –¥–≤—É—Å—Ç–æ—Ä–æ–Ω–Ω–µ–º —Ç–µ—Å—Ç–µ —Å –∫–∞–∂–¥–æ–π —Å—Ç–æ—Ä–æ–Ω—ã –ø–æ 2.5 –ø—Ä–æ—Ü–µ–Ω—Ç–æ–≤ (0.025)
# 3) –†–∞–∑–º–µ—Ä –≤—ã–±–æ—Ä–∫–∏ = 10
# 4) –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–µ–ø–µ–Ω–µ–π —Å–≤–æ–±–æ–¥—ã 10-1 = 9
# 5) –°—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ
sample = [2.51, 2.35, 2.74, 2.56, 2.40, 2.36, 2.65, 2.7, 2.67, 2.34]
sum = 0
count = 0
for i in sample:
    sum += i
    count += 1
sample_mean_manual = sum/count
print(f'–ó–∞–¥–∞—á–∞ 5.')
print(f'–ú–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–∂–∏–¥–∞–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ', sample_mean_manual)
# 6)  –ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–∞
sum02 = 0
count02 = 0
for i in sample:
    sum02 += (i - sample_mean_manual)**2
    count02 += 1
sample_var_manual = sum02/(count02 - 1)
print(f'–ù–µ—Å–º–µ—â—ë–Ω–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–∞', sample_var_manual)
# 7) –ù–µ—Å–º–µ—â—ë–Ω–Ω–æ–µ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ
sample_std_manual = sample_var_manual**0.5
print(f'–ù–µ—Å–º–µ—â—ë–Ω–Ω–æ–µ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ –≤—ã–±–æ—Ä–∫–∏ —Ä–∞–≤–Ω–æ', sample_std_manual)
